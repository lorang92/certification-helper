package demo.coresyntax;

public class DemoFractionalNumbers {

	public static void main(String[] args) {

		float myWeight = 57.5F;         		// My weight in kg this morning.
		double e = -1.60217656535E-19;  		// Charge on an electron, in Coulombs.
		
		System.out.println();	// Displays a blank line.
		System.out.println("I weigh " + myWeight + " kg");
		System.out.println("The charge on an electron is " + e + " Coulombs");
	}
}
