package demo.coresyntax;

public class DemoWholeNumbers {

	public static void main(String[] args) {
		
		byte examMark = 99;           			// Note, you'd probably use short or int here in reality!
		short meaningOfLife = 42;     			// See Hitchhiker's Guide to the Galaxy.
		int ukPopulation = 61500000;  			// Pretty densely populated eh?
		long grainsOfSandOnEarth = 120348759;	// I counted them all.
		
		System.out.println("Exam mark is " + examMark);
		System.out.println("Meaning of life is " + meaningOfLife);
		System.out.println("The UK population is " + ukPopulation);
		System.out.println("There are " + grainsOfSandOnEarth + " grains of sands on planet Earth");
	}
}
